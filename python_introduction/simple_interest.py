# simple_interest.py
# A Python script to calculate simple interest on an investment

# Define the variables
principal = 1000    # Principal amount in dollars ($1000)
rate = 0.05        # Annual interest rate as decimal (5%)
time = 3           # Time period in years (3 years)

# Calculate simple interest using the formula: I = P * R * T
interest = principal * rate * time

# Print the calculated interest in the specified format
print(f"The simple interest is: {interest}") 
